export function pickByLocation(
  arr: readonly string[],
  locationName: string
): string {
  let hash = 0;
  for (let i = 0; i < locationName.length; i++) {
    hash = (hash * 31 + locationName.charCodeAt(i)) | 0;
  }
  return arr[Math.abs(hash) % arr.length];
}
